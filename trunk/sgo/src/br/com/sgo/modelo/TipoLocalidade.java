package br.com.sgo.modelo;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.Table;

import br.com.caelum.vraptor.ioc.Component;

@Entity
@Component
@Table(name = "TIPOLOCALIDADE")
public class TipoLocalidade implements Serializable {

	private static final long serialVersionUID = 1L;

	@Id
	@Column(name = "tipolocalidade_id")
	@GeneratedValue(strategy = GenerationType.AUTO)
	private Long tipoLocalidade_id;

	@ManyToOne
	@JoinColumn(name = "empresa_id", updatable = true, nullable = false)
	private Empresa empresa;

	@ManyToOne
	@JoinColumn(name = "organizacao_id", updatable = true, nullable = false)
	private Organizacao organizacao;

	@Column(name = "nome")
	private String nome;

	@Column(name = "chave")
	private String chave;

	@Column(name = "isactive")
	private Boolean isActive;

	public Long getTipoLocalidade_id() {
		return tipoLocalidade_id;
	}

	public void setTipoLocalidade_id(Long tipoLocalidade_id) {
		this.tipoLocalidade_id = tipoLocalidade_id;
	}

	public Empresa getEmpresa() {
		return empresa;
	}

	public void setEmpresa(Empresa empresa) {
		this.empresa = empresa;
	}

	public Organizacao getOrganizacao() {
		return organizacao;
	}

	public void setOrganizacao(Organizacao organizacao) {
		this.organizacao = organizacao;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getChave() {
		return chave;
	}

	public void setChave(String chave) {
		this.chave = chave;
	}

	public Boolean getIsActive() {
		return isActive;
	}

	public void setIsActive(Boolean isActive) {
		this.isActive = isActive;
	}
}
